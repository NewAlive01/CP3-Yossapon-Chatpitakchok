row = int(input("Row :"))

for i in range(row):
    print(" "*(row -i -1)+"*"*(i*2+1))
    # row จำนวนบันทัด -i (i เริ่มตัวแรกที่ 0 ) แล้วลบด้วย 1 จะได้เป็นตัวว่าง " "  สมมุติใส่ row ไป4 จะเป็น 4 -0 -1 เท่ากับ 3 "  "
    # + "*" * (i*2+1) คือเอา "*" คูญ (i *2+1) 2*0 = 0 , 0+1 = 1 ก็จะเป็น " " 4ช่อง และ * 1ตัว ===   "    *"
